// Get color multiplier for pricing
export const getColorMultiplier = (color: string): number => {
  const multipliers: { [key: string]: number } = {
    'natural-black': 1.0,
    'dark-brown': 1.1,
    'medium-brown': 1.15
  };
  return multipliers[color] || 1.0;
};

// Get products by color
export const getProductsByColor = (color: string) => {
  const colorKey = color.toLowerCase().replace(' ', '-');
  return afroKinkyProducts[colorKey as keyof typeof afroKinkyProducts] || [];
};

// Get similar products (same color, different length)
export const getSimilarProducts = (productId: string, limit: number = 4) => {
  const product = getProductById(productId);
  if (!product) return [];
  
  const sameColorProducts = getProductsByColor(product.color);
  return sameColorProducts
    .filter(p => p.id !== productId)
    .slice(0, limit);
};